  <h3>The login and your public details using Facebook and Twitter</h3>
  <p>By adopting Facebook and Twitter API guidelines for third party applications, we can offer a safe and fast login for all Personal Trainers that join this site. Further benefits include a community of personal trainers who already understand on-line conduct and the benefits and pleasures of social networking. For this we are very grateful. </p>
  <p>By using the API of both Facebook and Twitter we have adopted a 100% safe process of login. Additionally  on Personal Trainer Wall we are only using the information that you have already made public on these applications.<br />
We understand that new users to the site may be justifiably cautious in participating in this method of login.<br /> 
Be assured that it is exactly your safety  and privacy that we have considered in adopting this model.  <br />
  <br />
  In regard to joining Personal trainer Wall, connecting to Third Party Applications, allowing/disallowing access and your privacy refer to <a href="http://www.facebook.com/privacy/explanation.php">Facebook's explanation of Privacy and Safety</a> and Twitter's <a href="http://support.twitter.com/forums/26810/entries/76052">Connecting to Third-Party Applications, Programs, Services, and Websites</a>.</p>
 
